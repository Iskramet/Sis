int		numeric(char l)
{
	if (l >= '0' && l <= '9')
		return (1);
	return (0);
}

int		ft_str_is_numeric(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (!(numeric(str[i])))
			return (0);
		i++;
	}
	return (1);
}
