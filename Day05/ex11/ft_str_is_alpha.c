int		alpha(char l)
{
	if (l >= 'a' && l <= 'z')
		return (1);
	if (l >= 'A' && l <= 'Z')
		return (1);
	return (0);
}

int		ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (!(alpha(str[i])))
			return (0);
		i++;
	}
	return (1);
}
